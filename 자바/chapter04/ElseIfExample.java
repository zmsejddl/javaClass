package chapter04;
//3일차 8번째
public class ElseIfExample {

	public static void main(String[] args) {

		/*
		 * else if문
		 * if(조건문1) {
		 *  실행문A
		 * }else if(조건문2) {
		 *  실행문B
		 * }else if(조건문3) {
		 *  실행문C
		 * }else {
		 *  실행문D
		 * }
		 * else 블록은 생략 가능하다
		 */
		
		int score = 75;
		
		if (score >= 90) {
			System.out.println("A");
		}else if(score >=80) {
			System.out.println("B");
		}else if(score >=70) {
			System.out.println("C");
		}else {
			System.out.println("D");
		}
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
